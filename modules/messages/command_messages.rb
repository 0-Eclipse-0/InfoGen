module Command_messages
  def Command_messages.info
    puts "\nName: InfoGen"
    puts "Author: Eclipse"
    puts "Version: 1.0.5"
    puts "Release Date: 11/8"
    puts "Update Date: 11/26"
  end
  def Command_messages.help
    puts "\nStart: The command that starts the actual program."
    puts "Purpose: The command that prints the reason for creating this program."
    puts "Help: The command that lists all other commands."
    puts "Info: The command for information about the program."
  end
  def Command_messages.purpose
    puts "\nThis program was created so people who"
    puts "want to conceal their identities online."
  end
end
